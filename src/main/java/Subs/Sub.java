package Subs;

public class Sub {
    private Boolean bebida, combo, galleta, papas;
    public String pan, queso, vegetales, salsas, termino, adiciones;

    public Sub(){
    }

    public String getpan(){
        return pan;
    }
    public void setpan(String pan) {
        this.pan = pan;
    }

    public String getqueso(){
        return queso;
    }
    public void setqueso(String queso) {
        this.queso = queso;
    }

    public String getvegetales(){
        return vegetales;
    }
    public void setvegetales(String vegetales) {
        this.vegetales = vegetales;
    }

    public String getsalsas(){
        return salsas;
    }
    public void setsalsas(String pan) {
        this.salsas = salsas;
    }

    public String gettermino(){
        return termino;
    }
    public void settermino(String termino) {
        this.termino = termino;
    }

    public String getadiciones(){
        return adiciones;
    }
    public void setadiciones(String adiciones) {
        this.adiciones = adiciones;
    }

    public boolean getbebida(){
        return bebida;
    }
    public void setbebida(boolean bebida) {
        this.bebida = bebida;
    }

    public boolean getcombo(){
        return combo;
    }
    public void setcombo(boolean combo) {
        this.combo = combo;
    }

    public boolean getgalleta(){
        return galleta;
    }
    public void setgalleta(boolean galleta) {
        this.galleta = galleta;
    }

    public boolean getpapas(){
        return papas;
    }
    public void setpapas(boolean papas) {
        this.papas = papas;
    }

}



